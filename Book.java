public class Book {

    private String Name;
    private String Author;
    private String Title;

    // Setters
    public void setName(String n) {
        Name = n;
    }

    public void setAuthor(String a) {
        Author = a;
    }

    public void setTitle(String t) {
        Title = t;
    }

    // Getters
    public String getName() {
        return Name;
    }

    public String getAuthor() {
        return Author;
    }

    public String getTitle() {
        return Title;
    }

}